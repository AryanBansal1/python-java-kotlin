package strings;

public class indexingOfString {
    public static void main (String args []){

        //  FOR INDEXING WE USE charAt()

        String name = "Hacker";
        //System.out.println(name.charAt(0));
        for(int i=0;i<name.length();i++){
            System.out.println(name.charAt(i));
        }
    }
}
