public class Typecasting {
    public static void main(String args []){
        //type conversion or widening
        int a = 8;
        float b = a;
        long c = a;
        System.out.println(b);
        System.out.println(c);

        // type casting or narrowing
        double d = 7.89;
        int f = (int)d;
        System.out.println(f);

        //pracice Q5
        int $ = 45;
        System.out.println($);
    }
    
}
